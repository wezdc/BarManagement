    private void btnEnlistarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEnlistarActionPerformed

        try
        {
            DefaultTableModel modelo = new DefaultTableModel();
            jtPersona.setModel(modelo);

            PreparedStatement ps = null;
            ResultSet rs = null;
            Conexion conn = new Conexion();
            Connection con = conn.getConexion();

            String sql = "SELECT Id, Nombre, Apellido_Paterno, Apellido_Materno, Edad FROM persona";
            ps = con.prepareStatement(sql);
            rs = ps.executeQuery();

            ResultSetMetaData rsMd = rs.getMetaData();
            int cantidadColumnas = rsMd.getColumnCount();

            modelo.addColumn("ID");
            modelo.addColumn("Nombre");
            modelo.addColumn("Apellido Paterno");
            modelo.addColumn("Apellido Materno");
            modelo.addColumn("Edad");

            while(rs.next())
            {
                Object[] filas = new Object[cantidadColumnas];

                for(int i = 0; i < cantidadColumnas; i++)
                {
                    filas[i] = rs.getObject(i+1);

                }

                modelo.addRow(filas);
            }

        }
        catch(SQLException ex)
        {
            System.err.println(ex.toString());
        }
        btnLimpiar.setEnabled(true);
    }//GEN-LAST:event_btnEnlistarActionPerformed


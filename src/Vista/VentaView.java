    private void jtPersonaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtPersonaMouseClicked

        PreparedStatement ps = null;
        ResultSet rs = null;

        try
        {
            Conexion objCon = new Conexion();
            Connection conn = objCon.getConexion();

            int Fila = jtPersona.getSelectedRow();
            String id= jtPersona.getValueAt(Fila, 0).toString();

            ps = conn.prepareStatement("SELECT Id, Nombre, Apellido_Paterno, Apellido_Materno, Edad FROM persona WHERE "
                + "Id=?");
            ps.setString(1, id);
            rs = ps.executeQuery();

            while(rs.next())
            {
                txtId.setText(rs.getString("Id"));
                txtStatus.setText(rs.getString("Nombre"));
                txtFecha.setText(rs.getString("Apellido_Paterno"));
                txtCliente.setText(rs.getString("Apellido_Materno"));
                txtUsuario.setText(rs.getString("Edad"));
                btnEliminar.setEnabled(true);
                btnModificar.setEnabled(true);
            }

        }
        catch(SQLException ex)
        {
            System.out.println(ex.toString());
        }
    }//GEN-LAST:event_jtPersonaMouseClicked

